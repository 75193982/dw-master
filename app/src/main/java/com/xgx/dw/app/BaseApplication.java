    /**
     * 获取版本号
     *
     * @return 当前应用的版本号
     */
    public static String getVersion() {
        try {
            PackageManager manager = mInstance.getPackageManager();
            PackageInfo info = manager.getPackageInfo(mInstance.getPackageName(), 0);
            String version = info.versionName;
            return version;
        } catch (Exception e) {
            e.printStackTrace();
            return "";
        }
    }

    public static String getVersionCode() {
        try {
            PackageInfo localPackageInfo = mInstance.getPackageManager().getPackageInfo(mInstance.getPackageName(), 0);
            String str = localPackageInfo.versionCode + "";
            return str;
        } catch (Exception localException) {
            localException.printStackTrace();
        }
        return "";
    }

